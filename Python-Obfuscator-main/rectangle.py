# The rectangle module has functions that perform
# calculations related to rectangles

# the area function accepts a rectangle's width and 
# lenmgth as arguments and returns the rectangle's area.
def area(width, length):
    return width * length

# the perimeter function accepts a rectangles width
# and length as arguments and retunrs the rectangles
# perimeter

def perimeter(width, length):
    return 2 * (width + length)
    